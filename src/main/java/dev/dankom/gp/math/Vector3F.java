package dev.dankom.gp.math;

public class Vector3F {

    public float x;
    public float y;
    public float z;

    public Vector3F() {
        this.x = 0f;
        this.y = 0f;
        this.z = 0f;
    }

    public Vector3F(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
